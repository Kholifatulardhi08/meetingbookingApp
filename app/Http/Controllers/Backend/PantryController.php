    {
        $pantries = Pantry::all();
        if ($request->has('search')) {
            $pantries = Pantry::where('food', 'like', "%{$request->search}%")->orWhere('drink', 'like', "%{$request->search}%")->get();
        }

        return view('pantries.index', compact('pantries'));
    }

